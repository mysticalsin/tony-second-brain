run_check() {
  python3 - \
    "$VIOLATIONS_FILE" \
    "$FP_LOG" \
    "$DREAM_DIGEST" \
    "$ESCALATIONS_DIR" \
    "$VAULT" \
    <<'CHECK_PY'
import sys, json, os, datetime, collections

violations_path = sys.argv[1]
fp_log_path     = sys.argv[2]
dream_digest    = sys.argv[3]
escalations_dir = sys.argv[4]
vault           = sys.argv[5]

os.makedirs(escalations_dir, exist_ok=True)

now = datetime.datetime.now(datetime.timezone.utc)
today_str = now.strftime("%Y-%m-%d")

records = []
with open(violations_path, encoding="utf-8") as fh:
    for raw in fh:
        raw = raw.strip()
        if not raw:
            continue
        try:
            records.append(json.loads(raw))
        except json.JSONDecodeError:
            pass

fp_records = []
if os.path.isfile(fp_log_path):
    with open(fp_log_path, encoding="utf-8") as fh:
        for raw in fh:
            raw = raw.strip()
            if not raw:
                continue
            try:
                fp_records.append(json.loads(raw))
            except json.JSONDecodeError:
                pass

cutoff_7d  = (now - datetime.timedelta(days=7)).isoformat(timespec="seconds")
cutoff_30d = (now - datetime.timedelta(days=30)).isoformat(timespec="seconds")

window_7d  = [r for r in records if r.get("ts", "") >= cutoff_7d]
window_30d = [r for r in records if r.get("ts", "") >= cutoff_30d]

oldest_ts = min((r.get("ts", "") for r in records), default="")
has_history_9w = bool(oldest_ts) and oldest_ts < (now - datetime.timedelta(days=63)).isoformat(timespec="seconds")

count_7d = len(window_7d)

cutoff_14d = (now - datetime.timedelta(days=14)).isoformat(timespec="seconds")
window_prior_7d = [r for r in records if cutoff_14d <= r.get("ts", "") < cutoff_7d]
count_prior_7d = len(window_prior_7d)
delta = count_7d - count_prior_7d

breaches = []

if count_7d > 10 and delta > 0:
    breaches.append(
        f"violations-per-week: {count_7d} in last 7d (threshold 10), delta={delta:+d} (rising)"
    )
if has_history_9w and count_7d > 5:
    breaches.append(
        f"violations-per-week (steady-state): {count_7d} in last 7d (threshold 5)"
    )

fp_30d = [r for r in fp_records if r.get("ts", "") >= cutoff_30d]
hook_flags_30d = [
    r for r in window_30d
    if r.get("source", "") in ("md-write-hook", "injection-scan")
]
total_flags = len(hook_flags_30d)
fp_count = len(fp_30d)
if total_flags > 0:
    fp_rate = fp_count / total_flags
    if fp_rate >= 0.05:
        breaches.append(
            f"false-positive-rate: {fp_rate*100:.1f}% over 30d ({fp_count}/{total_flags}) — threshold 5%"
        )

agent_state_dir = os.path.join(vault, "_agent_state")
theta_breaches = []
if os.path.isdir(agent_state_dir):
    for entry in sorted(os.listdir(agent_state_dir)):
        rep_path = os.path.join(agent_state_dir, entry, "reputation.json")
        if not os.path.isfile(rep_path):
            continue
        try:
            with open(rep_path, encoding="utf-8") as fh:
                rep = json.load(fh)
        except (json.JSONDecodeError, IOError):
            continue
        theta = rep.get("theta") or rep.get("reputation_theta")
        if theta is None:
            continue
        run_count = rep.get("run_count") or rep.get("runs", 0) or 0
        last_active = rep.get("last_active", "")
        is_active = run_count >= 10 or (last_active >= cutoff_30d)
        if not is_active:
            continue
        if float(theta) < 0.85:
            theta_breaches.append(f"{entry} theta={theta}")

if theta_breaches:
    breaches.append(
        "reputation-theta-below-floor: " + ", ".join(theta_breaches)
    )

if os.path.isfile(dream_digest):
    mtime = datetime.datetime.fromtimestamp(
        os.path.getmtime(dream_digest), tz=datetime.timezone.utc
    )
    age_h = (now - mtime).total_seconds() / 3600
    if age_h > 48:
        breaches.append(
            f"dream-loop-cadence: digest is {age_h:.1f}h old (threshold 48h)"
        )
else:
    breaches.append(
        "dream-loop-cadence: dream-digest.json not found — dream loop has never run"
    )

if breaches:
    esc_file = os.path.join(escalations_dir, f"conduct-breach-{today_str}.md")
    ts_str = now.isoformat(timespec="seconds")
    with open(esc_file, "a", encoding="utf-8") as fh:
        for b in breaches:
            line = f"[BREACH] {b}. Checked: {ts_str}\n"
            fh.write(line)
            print(line.rstrip(), file=sys.stderr)
    print(f"conduct-stats --check: {len(breaches)} breach(es) — see {esc_file}", file=sys.stderr)
    sys.exit(1)
else:
    print(
        f"conduct-stats --check: OK — "
        f"7d violations={count_7d}, fp_rate={fp_count}/{max(total_flags,1)}, "
        f"no theta breaches, dream digest present."
    )
    sys.exit(0)
CHECK_PY
}

