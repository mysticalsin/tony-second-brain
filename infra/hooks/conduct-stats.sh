#!/usr/bin/env bash
# conduct-stats.sh — Reads the conduct-violations.jsonl and prints a summary.
#
# Usage:
#   bash infra/hooks/conduct-stats.sh           # Full summary (stdout, no side-effects)
#   bash infra/hooks/conduct-stats.sh --dream   # Compact 7-day JSON digest for agent-dreaming
#   bash infra/hooks/conduct-stats.sh --check   # Check metrics vs targets; exit 1 + write escalation on breach
#   bash infra/hooks/conduct-stats.sh --md      # Write human dashboard to $VAULT/02_Areas/Conduct.md
#
# Output: plain text or JSON to stdout (--dream, default).
#         --check:  exits 0 (clean) or 1 (breach). Breach writes to Important/escalations/.
#         --md:     writes 02_Areas/Conduct.md; exits 0.
# Idempotent for all modes. Append-only log; never modified.
#
# Dependencies: jq (preferred) OR python3 stdlib (automatic fallback).
# bash -n clean; python3 -m py_compile clean.

set -uo pipefail

# ── Paths ────────────────────────────────────────────────────────────────────

VAULT="${VAULT_ROOT:?Set VAULT_ROOT to your vault path}"
VIOLATIONS_FILE="$VAULT/_agent_state/_conduct/conduct-violations.jsonl"
ESCALATIONS_DIR="$VAULT/Important/escalations"
CONDUCT_MD="$VAULT/02_Areas/Conduct.md"
METRICS_FILE="$VAULT/infra/conduct/conduct-success-metrics.md"
FP_LOG="$VAULT/_agent_state/_conduct/false-positive-log.jsonl"
DREAM_DIGEST="$VAULT/_agent_state/_conduct/dream-digest.json"

# ── Mode ─────────────────────────────────────────────────────────────────────

DREAM_MODE=0
CHECK_MODE=0
MD_MODE=0
[[ "${1:-}" == "--dream" ]] && DREAM_MODE=1
[[ "${1:-}" == "--check" ]] && CHECK_MODE=1
[[ "${1:-}" == "--md" ]]    && MD_MODE=1

# ── Handle missing / empty file ───────────────────────────────────────────────

if [[ ! -f "$VIOLATIONS_FILE" ]] || [[ ! -s "$VIOLATIONS_FILE" ]]; then
  if [[ $DREAM_MODE -eq 1 ]]; then
    printf '{"dream":true,"period_days":7,"generated":"%s","status":"no_data","top_violations":[],"agent_profiles":[],"high_severity":[]}\n' \
      "$(date -Iseconds)"
  elif [[ $CHECK_MODE -eq 1 ]]; then
    printf 'conduct-stats --check: log missing or empty — all violation-count metrics pass (0 violations).\n'
    python3 - "$DREAM_DIGEST" "$ESCALATIONS_DIR" <<'CHECK_NODATA'
import sys, os, datetime

dream_digest = sys.argv[1]
escalations_dir = sys.argv[2]
os.makedirs(escalations_dir, exist_ok=True)
now = datetime.datetime.now(datetime.timezone.utc)
breaches = []

# Metric 5 — dream loop cadence (48h hard limit)
if os.path.isfile(dream_digest):
    mtime = datetime.datetime.fromtimestamp(os.path.getmtime(dream_digest), tz=datetime.timezone.utc)
    age_h = (now - mtime).total_seconds() / 3600
    if age_h > 48:
        breaches.append(f"dream-loop-cadence: digest is {age_h:.1f}h old (threshold 48h)")
else:
    breaches.append("dream-loop-cadence: dream-digest.json not found — dream loop has never run")

if breaches:
    date_str = now.strftime("%Y-%m-%d")
    esc_file = os.path.join(escalations_dir, f"conduct-breach-{date_str}.md")
    line = "[BREACH] " + "; ".join(breaches) + f". Checked: {now.isoformat(timespec='seconds')}"
    with open(esc_file, "a", encoding="utf-8") as fh:
        fh.write(line + "\n")
    print(line, file=sys.stderr)
    sys.exit(1)
sys.exit(0)
CHECK_NODATA
  elif [[ $MD_MODE -eq 1 ]]; then
    mkdir -p "$(dirname "$CONDUCT_MD")"
    python3 - "$CONDUCT_MD" <<'MD_NODATA'
import sys, datetime
out = sys.argv[1]
now = datetime.datetime.now().astimezone().isoformat(timespec="seconds")
content = f"""---
type: dashboard
topic: conduct
generated: {now}
---

# Conduct Dashboard

> Generated by `conduct-stats.sh --md` on {now}.

**No violations on record.** The log is empty or missing — the conduct system is clean.

| Metric | Value |
|---|---|
| Total violations | 0 |
| Last 7 days | 0 |
| High severity | 0 |
"""
with open(out, "w", encoding="utf-8") as fh:
    fh.write(content)
print(f"conduct-stats --md: wrote {out}")
MD_NODATA
  else
    printf 'conduct-stats: no violations on record (file missing or empty).\n'
    printf 'File: %s\n' "$VIOLATIONS_FILE"
  fi
  exit 0
fi

