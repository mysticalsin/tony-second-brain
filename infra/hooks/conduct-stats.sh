run_jq_full() {
  local f="$VIOLATIONS_FILE"

  printf '\n══════════════════════════════════════════════════════\n'
  printf '  CONDUCT VIOLATIONS SUMMARY\n'
  printf '  File: %s\n' "$f"
  printf '  Generated: %s\n' "$(date -Iseconds)"
  printf '══════════════════════════════════════════════════════\n\n'

  local total
  total=$(jq -s 'length' "$f")
  printf 'TOTAL VIOLATIONS: %s\n\n' "$total"

  printf '── BY SOURCE ─────────────────────────────────────────\n'
  jq -rs '[.[] | .source] | group_by(.) | map({source: .[0], count: length}) | sort_by(-.count) | .[] | "\(.count)  \(.source)"' "$f"
  printf '\n'

  printf '── BY RULE ───────────────────────────────────────────\n'
  jq -rs '[.[] | .rule] | group_by(.) | map({rule: .[0], count: length}) | sort_by(-.count) | .[] | "\(.count)  \(.rule)"' "$f"
  printf '\n'

  printf '── BY SEVERITY ───────────────────────────────────────\n'
  jq -rs '[.[] | .severity] | group_by(.) | map({sev: .[0], count: length}) | sort_by(-.count) | .[] | "\(.count)  \(.sev)"' "$f"
  printf '\n'

  printf '── BY AGENT ──────────────────────────────────────────\n'
  jq -rs '[.[] | .agent] | group_by(.) | map({agent: .[0], count: length}) | sort_by(-.count) | .[] | "\(.count)  \(.agent)"' "$f"
  printf '\n'

  printf '── LAST 10 (newest first) ────────────────────────────\n'
  jq -rs 'sort_by(.ts) | reverse | .[0:10] | .[] | "[\(.ts)]  [\(.severity)]  \(.agent)  \(.rule)\n  \(.detail)\n  file: \(.file // "null")"' "$f"
  printf '\n══════════════════════════════════════════════════════\n\n'
}

run_jq_dream() {
  local f="$VIOLATIONS_FILE"
  local cutoff
  cutoff=$(python3 -c "
import datetime
d = datetime.datetime.now(datetime.timezone.utc) - datetime.timedelta(days=7)
print(d.isoformat(timespec='seconds'))
")

  jq -rs --arg cutoff "$cutoff" '
    map(select(.ts >= $cutoff)) as $window |
    {
      dream: true,
      period_days: 7,
      generated: now | todate,
      total_in_window: ($window | length),
      top_violations: (
        $window
        | group_by(.rule)
        | map({
            rule: .[0].rule,
            count: length,
            dominant_severity: (group_by(.severity) | sort_by(-length) | .[0][0].severity),
            agents: (map(.agent) | unique),
            example: .[0].detail
          })
        | sort_by(-.count)
        | .[0:5]
      ),
      agent_profiles: (
        $window
        | group_by(.agent)
        | map({
            agent: .[0].agent,
            count: length,
            top_rule: (group_by(.rule) | sort_by(-length) | .[0][0].rule),
            high_count: (map(select(.severity == "high")) | length)
          })
        | sort_by(-.count)
      ),
      high_severity_last7: (
        $window
        | map(select(.severity == "high"))
        | sort_by(.ts) | reverse
        | .[0:10]
        | map({ts: .ts, agent: .agent, rule: .rule, detail: .detail, file: .file})
      ),
      rules_with_3plus_same_day: (
        $window
        | group_by(.ts[0:10])
        | map(
            {
              day: .[0].ts[0:10],
              rules: (group_by(.rule) | map(select(length >= 3)) | map({rule: .[0].rule, count: length}))
            }
          )
        | map(select(.rules | length > 0))
      )
    }
  ' "$f"
}

