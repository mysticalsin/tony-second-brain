run_python_full() {
  python3 - "$VIOLATIONS_FILE" <<'PYEOF'
import sys, json, collections, datetime

path = sys.argv[1]

records = []
with open(path, encoding="utf-8") as fh:
    for lineno, raw in enumerate(fh, 1):
        raw = raw.strip()
        if not raw:
            continue
        try:
            records.append(json.loads(raw))
        except json.JSONDecodeError as exc:
            print(f"[WARN] line {lineno} is not valid JSON: {exc}", file=sys.stderr)

sep = "══════════════════════════════════════════════════════"
print(f"\n{sep}")
print("  CONDUCT VIOLATIONS SUMMARY")
print(f"  File: {path}")
print(f"  Generated: {datetime.datetime.now().astimezone().isoformat(timespec='seconds')}")
print(f"{sep}\n")
print(f"TOTAL VIOLATIONS: {len(records)}\n")

def top_counts(records, key, title):
    counts = collections.Counter(r.get(key, "unknown") for r in records)
    print(f"── {title} {'─' * (50 - len(title) - 4)}")
    for val, cnt in counts.most_common():
        print(f"  {cnt:>4}  {val}")
    print()

top_counts(records, "source", "BY SOURCE")
top_counts(records, "rule",   "BY RULE")
top_counts(records, "severity","BY SEVERITY")
top_counts(records, "agent",  "BY AGENT")

print("── LAST 10 (newest first) ────────────────────────────")
sorted_recs = sorted(records, key=lambda r: r.get("ts", ""), reverse=True)
for r in sorted_recs[:10]:
    ts       = r.get("ts", "?")
    sev      = r.get("severity", "?")
    agent    = r.get("agent", "?")
    rule     = r.get("rule", "?")
    detail   = r.get("detail", "?")
    file_    = r.get("file") or "null"
    print(f"[{ts}]  [{sev}]  {agent}  {rule}")
    print(f"  {detail}")
    print(f"  file: {file_}")
print(f"\n{sep}\n")
PYEOF
}

run_python_dream() {
  python3 - "$VIOLATIONS_FILE" <<'PYEOF'
import sys, json, collections, datetime

path = sys.argv[1]
now = datetime.datetime.now(datetime.timezone.utc)
cutoff = now - datetime.timedelta(days=7)

records = []
with open(path, encoding="utf-8") as fh:
    for raw in fh:
        raw = raw.strip()
        if not raw:
            continue
        try:
            records.append(json.loads(raw))
        except json.JSONDecodeError:
            pass

window = [r for r in records if r.get("ts", "") >= cutoff.isoformat(timespec="seconds")]

rule_groups = collections.defaultdict(list)
for r in window:
    rule_groups[r.get("rule", "unknown")].append(r)

top_violations = []
for rule, items in sorted(rule_groups.items(), key=lambda x: -len(x[1]))[:5]:
    sev_counts = collections.Counter(r.get("severity", "?") for r in items)
    dominant_sev = sev_counts.most_common(1)[0][0] if sev_counts else "?"
    agents = list({r.get("agent", "unknown") for r in items})
    top_violations.append({
        "rule": rule,
        "count": len(items),
        "dominant_severity": dominant_sev,
        "agents": agents,
        "example": items[0].get("detail", "?"),
    })

agent_groups = collections.defaultdict(list)
for r in window:
    agent_groups[r.get("agent", "unknown")].append(r)

agent_profiles = []
for agent, items in sorted(agent_groups.items(), key=lambda x: -len(x[1])):
    rule_ctr = collections.Counter(r.get("rule", "?") for r in items)
    top_rule = rule_ctr.most_common(1)[0][0] if rule_ctr else "?"
    high_cnt = sum(1 for r in items if r.get("severity") == "high")
    agent_profiles.append({
        "agent": agent,
        "count": len(items),
        "top_rule": top_rule,
        "high_count": high_cnt,
    })

high_sev = sorted(
    [r for r in window if r.get("severity") == "high"],
    key=lambda r: r.get("ts", ""),
    reverse=True,
)[:10]
high_list = [{"ts": r.get("ts"), "agent": r.get("agent"), "rule": r.get("rule"),
              "detail": r.get("detail"), "file": r.get("file")} for r in high_sev]

day_rule = collections.defaultdict(lambda: collections.defaultdict(int))
for r in window:
    day = r.get("ts", "")[:10]
    rule = r.get("rule", "?")
    day_rule[day][rule] += 1

rules_3plus_day = []
for day, rule_counts in sorted(day_rule.items()):
    spikes = [{"rule": rule, "count": cnt} for rule, cnt in rule_counts.items() if cnt >= 3]
    if spikes:
        rules_3plus_day.append({"day": day, "rules": spikes})

digest = {
    "dream": True,
    "period_days": 7,
    "generated": now.isoformat(timespec="seconds"),
    "total_in_window": len(window),
    "top_violations": top_violations,
    "agent_profiles": agent_profiles,
    "high_severity_last7": high_list,
    "rules_with_3plus_same_day": rules_3plus_day,
}
print(json.dumps(digest, ensure_ascii=False, indent=2))
PYEOF
}

