run_md() {
  python3 - "$VIOLATIONS_FILE" "$CONDUCT_MD" <<'MD_PY'
import sys, json, collections, datetime, os

violations_path = sys.argv[1]
out_path        = sys.argv[2]

os.makedirs(os.path.dirname(out_path), exist_ok=True)

now = datetime.datetime.now().astimezone()
now_iso = now.isoformat(timespec="seconds")
now_str = now.strftime("%Y-%m-%d %H:%M %Z")

records = []
with open(violations_path, encoding="utf-8") as fh:
    for lineno, raw in enumerate(fh, 1):
        raw = raw.strip()
        if not raw:
            continue
        try:
            records.append(json.loads(raw))
        except json.JSONDecodeError:
            pass

cutoff_7d = (datetime.datetime.now(datetime.timezone.utc) - datetime.timedelta(days=7)).isoformat(timespec="seconds")
window_7d = [r for r in records if r.get("ts", "") >= cutoff_7d]

total = len(records)
total_7d = len(window_7d)
high_total = sum(1 for r in records if r.get("severity") == "high")
high_7d    = sum(1 for r in window_7d if r.get("severity") == "high")

cutoff_14d = (datetime.datetime.now(datetime.timezone.utc) - datetime.timedelta(days=14)).isoformat(timespec="seconds")
prior_7d = [r for r in records if cutoff_14d <= r.get("ts", "") < cutoff_7d]
delta_7d = total_7d - len(prior_7d)
delta_str = f"+{delta_7d}" if delta_7d > 0 else str(delta_7d)
if delta_7d < 0:
    trend_emoji = "(improving)"
elif delta_7d == 0:
    trend_emoji = "(flat)"
else:
    trend_emoji = "(worsening)"

def top_table(records, key, header, max_rows=10):
    counts = collections.Counter(r.get(key, "unknown") for r in records)
    rows = [f"| {v} | {c} |" for v, c in counts.most_common(max_rows)]
    if not rows:
        return f"| — | — |\n"
    return "\n".join(rows) + "\n"

by_rule_all   = top_table(records,   "rule",     "Rule (all-time)")
by_rule_7d    = top_table(window_7d, "rule",     "Rule (7d)")
by_source_all = top_table(records,   "source",   "Source")
by_sev_all    = top_table(records,   "severity", "Severity")
by_agent_all  = top_table(records,   "agent",    "Agent")

sorted_recs = sorted(records, key=lambda r: r.get("ts", ""), reverse=True)
last10_rows = []
for r in sorted_recs[:10]:
    ts     = r.get("ts", "?")
    sev    = r.get("severity", "?")
    agent  = r.get("agent", "?")
    rule   = r.get("rule", "?")
    detail = r.get("detail", "?")[:80].replace("|", "·")
    last10_rows.append(f"| {ts} | {sev} | {agent} | {rule} | {detail} |")
last10_block = "\n".join(last10_rows) if last10_rows else "| — | — | — | — | — |"

content = f"""---
type: dashboard
topic: conduct
generated: {now_iso}
---

# Conduct Dashboard

> Auto-generated by `conduct-stats.sh --md`. Last updated: **{now_str}**.
> Source: `_agent_state/_conduct/conduct-violations.jsonl` — {total} total records.

---

## Summary

| Metric | All-time | Last 7 days | 7d delta |
|---|---|---|---|
| Total violations | {total} | {total_7d} | {delta_str} {trend_emoji} |
| High severity | {high_total} | {high_7d} | — |

---

## By Rule (all-time)

| Rule | Count |
|---|---|
{by_rule_all}
## By Rule (last 7 days)

| Rule | Count |
|---|---|
{by_rule_7d}
## By Source

| Source | Count |
|---|---|
{by_source_all}
## By Severity

| Severity | Count |
|---|---|
{by_sev_all}
## By Agent

| Agent | Count |
|---|---|
{by_agent_all}
---

## Last 10 Violations (newest first)

| Timestamp | Severity | Agent | Rule | Detail (truncated) |
|---|---|---|---|---|
{last10_block}

---

*Run `bash infra/hooks/conduct-stats.sh --md` to refresh. Run `--check` for automated breach detection.*
"""

with open(out_path, "w", encoding="utf-8") as fh:
    fh.write(content)

print(f"conduct-stats --md: wrote {out_path}")
MD_PY
}

